package Parte01;

public class P1PG51 {
    public static void main(String[] args) {
        double alturaMuro = 3;
        double qtdDeDias;

        qtdDeDias = 2*alturaMuro - 1.5;

        System.out.println("O caracol levou: "+qtdDeDias+" dias para subir um muro de 3 m");

        alturaMuro = 13;

        qtdDeDias = 2*alturaMuro - 1.5;

        System.out.println("O caracol levou: "+qtdDeDias+" dias para subir um muro de 13 m");

    }
}
